#define OMX_MMMIXER_DATAPATH(_datapath_,_rendertype_,_streamid_)\
        {\
        static char str[50];\
        int OMX_MMMIXER_DATAPATH_instreamid = ((_streamid_ >> 4) & 0xF) - 1;\
        int OMX_MMMIXER_DATAPATH_outstreamid = (_streamid_ & 0xF) - 1;\
        OMX_MMMIXER_ITOA(str, OMX_MMMIXER_DATAPATH_instreamid)\
        strcpy((char*)_datapath_,(char*)":i");\
        strcat((char*)_datapath_,(char*)str);\
        strcat((char*)_datapath_,(char*)":o");\
        OMX_MMMIXER_ITOA(str, OMX_MMMIXER_DATAPATH_outstreamid)\
        strcat((char*)_datapath_,(char*)str);\
        strcat((char*)_datapath_,(char*)"/codec\0");\
        }        

#define OMX_MMMIXER_DATAPATH_TEE(_datapath_,_rendertype_,_streamid_)\
        {\
        static char str[50];\
        int OMX_MMMIXER_DATAPATH_instreamid = ((_streamid_ >> 4) & 0xF) - 1;\
        int OMX_MMMIXER_DATAPATH_outstreamid = (_streamid_ & 0xF) - 1;\
        OMX_MMMIXER_ITOA(str, OMX_MMMIXER_DATAPATH_instreamid)\
        strcpy((char*)_datapath_,(char*)":i");\
        strcat((char*)_datapath_,(char*)str);\
        strcat((char*)_datapath_,(char*)":o");\
        OMX_MMMIXER_ITOA(str, OMX_MMMIXER_DATAPATH_outstreamid)\
        strcat((char*)_datapath_,(char*)str);\
        strcat((char*)_datapath_,(char*)"/eteedn:i0:o0/codec\0");\
        }

#define OMX_MMMIXER_ITOA(_alpha_,_integer_) \
{\
    static char OMX_MMMIXER_ITOA_str[50];\
    int i = sizeof(OMX_MMMIXER_ITOA_str) - 1;\
    do {\
        OMX_MMMIXER_ITOA_str[--i] = '0' + _integer_ % 10;\
    } while ((_integer_ = _integer_ / 10) && i > 0);\
    strcpy(_alpha_,&OMX_MMMIXER_ITOA_str[i]);\
}    

                    
